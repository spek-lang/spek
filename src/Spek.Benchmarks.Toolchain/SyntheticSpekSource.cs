namespace Spek.Benchmarks.Toolchain;

/// <summary>
/// Deterministic synthetic .spek source of an exact requested line count.
/// The density is modeled on the shipped samples (HelloBank, Telemetry,
/// demos/fleet): each repeated cell carries five messages with fields, a
/// two-behavior actor (init, field mutation, a switch with a `when`-guarded
/// pattern, a `reader on` handler with a return-reply, a `become`
/// transition), a single-behavior actor, and a module with a method. Cells
/// repeat with renamed identifiers (Desk0/Desk1/...), so a 5,000-line file
/// stresses the symbol table with hundreds of distinct declarations, not one
/// declaration parsed many times. No Random, no DateTime — the same line
/// count always yields the identical string, so benchmark runs compare
/// byte-identical inputs.
/// </summary>
public static class SyntheticSpekSource
{
    private const int HeaderLines = 3;
    private const int FooterLines = 25;
    private const int CellLines   = 72;

    /// <summary>Generates a Spek source file with exactly
    /// <paramref name="lines"/> lines ('\n' separated, no trailing newline).
    /// Whole cells fill as much of the budget as possible; comment filler
    /// lines pad the remainder so per-KLOC math stays honest. Minimum size is
    /// one cell plus header and footer (100 lines).</summary>
    public static string Generate(int lines)
    {
        if (lines < HeaderLines + CellLines + FooterLines)
            throw new ArgumentOutOfRangeException(nameof(lines),
                $"minimum synthetic source is {HeaderLines + CellLines + FooterLines} lines");

        var cells = (lines - HeaderLines - FooterLines) / CellLines;
        var filler = lines - HeaderLines - FooterLines - cells * CellLines;

        var text = new List<string>(lines)
        {
            "// Synthetic Spek source — deterministic, generated by SyntheticSpekSource.",
            "namespace ToolchainBench;",
            "",
        };

        for (int i = 0; i < cells; i++)
            AppendCell(text, i);

        for (int i = 0; i < filler; i++)
            text.Add($"// filler {i} — pads to the exact requested line count; lexed and dropped.");

        AppendFooter(text);

        if (text.Count != lines)
            throw new InvalidOperationException(
                $"generator drift: produced {text.Count} lines, wanted {lines}");

        return string.Join("\n", text);
    }

    /// <summary>Number of repeated cells a <paramref name="lines"/>-sized
    /// source contains; cell indices run 0..Count-1. Benchmarks use this to
    /// aim cursors and corruption at the middle or last cell.</summary>
    public static int CellCount(int lines) =>
        (lines - HeaderLines - FooterLines) / CellLines;

    private static void AppendCell(List<string> t, int i)
    {
        t.Add($"// ─── Cell {i}: order desk + journal + pricing ─────────────────────────────");
        t.Add($"message Submit{i}(int orderId, decimal amount);");
        t.Add($"message Cancel{i}(int orderId);");
        t.Add($"message QueryState{i}();");
        t.Add($"message StateReply{i}(int accepted, int rejected, decimal revenue);");
        t.Add($"message Audit{i}(string entry, long stamp);");
        t.Add("");
        t.Add($"actor Desk{i}");
        t.Add("{");
        t.Add("    int accepted = 0;");
        t.Add("    int rejected = 0;");
        t.Add("    decimal revenue = 0.00m;");
        t.Add("    ActorRef journal;");
        t.Add("    init(ActorRef j)");
        t.Add("    {");
        t.Add("        journal = j;");
        t.Add("        become Accepting;");
        t.Add("    }");
        t.Add("");
        t.Add("    behavior Accepting");
        t.Add("    {");
        t.Add($"        on Submit{i} s =>");
        t.Add("        {");
        t.Add("            switch (s.amount)");
        t.Add("            {");
        t.Add("                case decimal a when (a > 0.00m):");
        t.Add("                    accepted = accepted + 1;");
        t.Add("                    revenue = revenue + a;");
        t.Add("                    break;");
        t.Add("                default:");
        t.Add("                    rejected = rejected + 1;");
        t.Add("                    break;");
        t.Add("            }");
        t.Add($"            journal.Tell(new Audit{i}(\"submit\", Pricing{i}.Stamp(s.orderId)));");
        t.Add("        }");
        t.Add("");
        t.Add($"        on Cancel{i} c =>");
        t.Add("        {");
        t.Add("            rejected = rejected + 1;");
        t.Add("            if (rejected > 100)");
        t.Add("            {");
        t.Add("                become Draining;");
        t.Add("            }");
        t.Add("        }");
        t.Add("");
        t.Add($"        reader on QueryState{i} => return new StateReply{i}(accepted, rejected, revenue);");
        t.Add("    }");
        t.Add("");
        t.Add("    behavior Draining");
        t.Add("    {");
        t.Add($"        on Cancel{i} c => {{ rejected = rejected + 1; }}");
        t.Add($"        on QueryState{i} => {{ sender.Tell(new StateReply{i}(accepted, rejected, revenue)); }}");
        t.Add("    }");
        t.Add("}");
        t.Add("");
        t.Add($"actor Journal{i}");
        t.Add("{");
        t.Add("    long entries = 0;");
        t.Add("    behavior Default");
        t.Add("    {");
        t.Add($"        on Audit{i} a => {{ entries = entries + 1; }}");
        t.Add("    }");
        t.Add("}");
        t.Add("");
        t.Add($"module Pricing{i}");
        t.Add("{");
        t.Add("    public long Stamp(int orderId)");
        t.Add("    {");
        t.Add("        return orderId * 31L;");
        t.Add("    }");
        t.Add("}");
        t.Add("");
    }

    private static void AppendFooter(List<string> t)
    {
        t.Add("message Kickoff();");
        t.Add("");
        t.Add("actor Root");
        t.Add("{");
        t.Add("    init() { become Boot; }");
        t.Add("");
        t.Add("    behavior Boot");
        t.Add("    {");
        t.Add("        on Kickoff k =>");
        t.Add("        {");
        t.Add("            ActorRef journal = spawn<Journal0>();");
        t.Add("            ActorRef desk = spawn<Desk0>(journal);");
        t.Add("            desk.Tell(new Submit0(1, 250.00m));");
        t.Add("            desk.Tell(new Cancel0(1));");
        t.Add("        }");
        t.Add("    }");
        t.Add("}");
        t.Add("");
        t.Add("program Main");
        t.Add("{");
        t.Add("    var system = new ActorSystem(\"toolchain-bench\");");
        t.Add("    ActorRef root = system.Spawn<Root>();");
        t.Add("    root.Tell(new Kickoff());");
        t.Add("    system.AwaitTermination();");
        t.Add("}");
    }
}
